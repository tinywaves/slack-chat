# 模拟浏览器请求头,减少被识别为爬虫的概率
headers = {
    'User-Agent': random.choice(agents)
}
# 小说目录基准地址
book_categories_base_url = 'http://i.258zw.com/wapbook-1852'
# 小说内容基准地址
book_text_base_url = 'http://i.258zw.com/'
# 目录页面地址
book_categories_url = []
# 小说章节地址列表
chapter_url_list = []
# 小说保存地址
save_name = '御天邪神.txt'
# 已经爬取的小说的章节数目
count = 0
# 章节标题的匹配规则
# title_matching_rule = "<div class='nr_title' id='nr_title'>(.*?)</div>"
title_matching_rule = '<title>(.*?)</title>'
# 章节内容的匹配规则
content_matching_rule = '<br/>&nbsp;&nbsp;&nbsp;&nbsp;(.*?)<br/>'

# 获取所有的目录页面
index = 1
while index < 211:
    temp_url = book_categories_base_url + '_' + str(index)
    book_categories_url.append(temp_url)
    index += 1

# 对目录页面地址进行遍历
for category in book_categories_url:
    # 通过requests库的get方法发出请求后得到响应
    response = requests.get(category, headers=headers)
    # 设置编码格式
    response.encoding = 'gbk'
    # 获取对应目录页面的相关信息
    information = response.text
    # 获取页面href的匹配规则
    matching_rule = "href='/wapbook-1852(.*?)'"
    # 获取小说章节的href
    chapter_href_list = re.findall(matching_rule, information)
    # 对小说章节的href进行遍历
    for chapter_href in chapter_href_list:
        temp_url = book_text_base_url + 'wapbook-1852' + chapter_href
        chapter_url_list.append(temp_url)

with open(save_name, 'a+', encoding='gbk') as f:
    # 对小说章节地址列表进行遍历
    for chapter_url in chapter_url_list:
        print(chapter_url)
        # 一直向小说章节所在地址发送请求并获得响应,直到响应里没有“503 Service Temporarily Unavailable”
        while 1:
            response_text = requests.get(chapter_url, headers=headers)
            if '503 Service Temporarily Unavailable' not in response_text.text:
                break
            else:
                print('数据遗漏,等待3s')
                time.sleep(3)
        # 设定编码格式
        response_text.encoding = 'gbk'
        # 获取小说标题
        title = re.findall(title_matching_rule, response_text.text)
        # 正文内容
        content = re.findall(content_matching_rule, response_text.text)
        # 写入小说标题
        f.write('--------' + title[0] + '--------' + '\n')
        # 写入小说正文
        for e in content:
            f.write(e + '\n')

        count += 1
        print('第{}章 标题 : {} 爬取完毕!'.format(count, title[0]))

